function makeProduct({
  id,
  title,
  price,
  sku,
  image,
  sizes,
  source,
  status = 'active',
  isOriginal = true,
  variantsCount = 1,
  views = 0,
  viewsDelta = 0,
  orders = 0,
  ordersDelta = 0,
  addedAt,
  category = 'Одежда',
  kind = 'Худи',
  brand = 'Carne Bollente',
}) {
  return {
    id,
    title,
    price,
    sku,
    image,
    sizes,
    source, // 'china' | 'stock'
    status, // 'active' | 'draft' | 'archived'
    isOriginal,
    variantsCount,
    views,
    viewsDelta,
    orders,
    ordersDelta,
    addedAt,
    category,
    kind,
    brand,
  };
}

